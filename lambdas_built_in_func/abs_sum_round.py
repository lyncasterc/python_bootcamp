# sum -  takes an iterable, returns the sum
#  takes an optional start, which will then add everything + the start

nums = [1,2]

print(sum(nums)) #adds 1 and 2 = 3

print(sum(nums, 2)) #adds 1,2 and the start=2, so sum = 5


