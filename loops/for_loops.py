#for loops do something for each thing in a iterable object - a collection of items like lists, strings, a range.

for number in range(1, 8): 
  print(number)

#number is a variable that represents the each number in the range as it loops over. So number = 1, then number = 2, and so on, until it hits the end.
 

for char in 'coffee':
  print(char)
